from collections import namedtuple

Marks = namedtuple("Marks", ["алгебра", "геометрія", "історія", "інформатика", "географія"])

student1 = Marks(алгебра=10, геометрія=9, історія=8, інформатика=12, географія=11)
student2 = Marks(алгебра=7, геометрія=8, історія=6, інформатика=9, географія=7)
student3 = Marks(алгебра=12, геометрія=12, історія=10, інформатика=11, географія=12)

print("Оцінки учнів:")
print("Учень 1:", student1)
print("Учень 2:", student2)
print("Учень 3:", student3)
