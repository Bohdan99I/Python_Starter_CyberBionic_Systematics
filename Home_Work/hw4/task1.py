a = int(input("Введіть число a (менше за b): "))
b = int(input("Введіть число b (більше за a): "))

if a >= b or a < 1:
    print("Помилка: a повинно бути менше за b, і обидва мають бути натуральними числами.")
else:
    total = sum(range(a, b + 1))
    print("Сума всіх натуральних чисел від", a, "до", b, "дорівнює:", total)